    private void cargarComboBox() {
        try {
            String sqlComb = "SELECT * FROM comustible";
            ps = conn.prepareStatement(sqlComb);
            rs = ps.executeQuery();
            
            while (rs.next()) {
                modeloCb.addElement(rs.getString("idComb") + " " + rs.getString("nomComb"));
            }
            
            cbxCombustible.setModel(modeloCb);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al obtener los valores: " + ex);
        }
    }
    
    private void cargarTabla() {
        try {
            String titulos[] = {"ID", "Fecha", "Cliente", "Combustible", "Galones", "Total", "ID Empleado"};
            String registros[] = new String[7];
            
            modelo = new DefaultTableModel(null, titulos);
            
            String sql = "SELECT * FROM ventasi";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            
            while (rs.next()) {
                registros[0] = rs.getString("idVenta");
                registros[1] = rs.getString("fechaVenta");
                registros[2] = rs.getString("cedCliente");
                registros[3] = rs.getString("gasVen");
                registros[4] = rs.getString("galonesVen");
                registros[5] = rs.getString("totalVenta");
                registros[6] = rs.getString("idEmpVen");
                
                modelo.addRow(registros);
            }
            
            tblVentas.setModel(modelo);
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error obteniendo las ventas: " + ex);
        }
        
    }
    
