    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            // TODO add your handling code here:
            String selectedItem[] = cbxCombustible.getSelectedItem().toString().split(" ");
            LocalDate fecha = LocalDate.now();
            
            String sqlSelectValGal = "SELECT valGal FROM comustible WHERE idComb = " + selectedItem[0];
            
            ps = conn.prepareStatement(sqlSelectValGal);
            rs = ps.executeQuery();
            
            if (rs.next()) {
                double gal = Double.parseDouble(txtGalones.getText());
                double valTotal = gal * Double.parseDouble(rs.getString("valGal"));
                
                String sqlInsert = "INSERT INTO ventasi VALUES(NULL, '" + fecha + "', '"
                    + txtCedCliente.getText() + "', '" + selectedItem[0] + "', '"
                    + txtGalones.getText() + "', " + valTotal + ", " + idEmp + ")";
                
                ps = conn.prepareStatement(sqlInsert);
                int result = ps.executeUpdate();
                
                if (result > 0) {
                    JOptionPane.showMessageDialog(null, "Se guardÃ³ la venta");
                    
                    cargarTabla();
                }
            }
            
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

